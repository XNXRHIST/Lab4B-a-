/*Обчислити суму квадратів всіх парних чисел у діапазоні [1;20].
* з використанням циклу з параметром

*/
#include <stdio.h>//Піключаємо необхідну для роботи бібліотеку

int main() {
    int sum = 0;//оголошення зміних

    for (int i = 2; i <= 20; i += 2) {// Обчислення 
        sum += i * i;
    }

    printf("Сума квадратів всіх парних чисел у діапазоні [1;20]: %d\n", sum);//Виведення результату на екран юзера

    return 0;//Кінець програми
}

